#include <bits/stdc++.h>
using namespace std;

int main()
{

    long long int a, b;
    cin >> a >> b;

    long long int add = a + b;
    long long int mul = a * b;
    cout << a << " + " << b << " = " << add << endl;
    cout << a << " * " << b << " = " << mul << endl;
    cout << a << " - " << b << " = " << a - b << endl;

    return 0;
}